#EL DISEÑO EXPERIMENTAL CONSISTE EN ORGANIZAR UN PROCEDIMIENTO A TRAVES DEL CUAL SE OBTENGAN LOS DATOS NECESARIOS PARA RESPONDER UNA PREGUNTA DE CIENCIA DE DATOS DE FORMA EFECTIVA Y CONCRETA
#ESTE PROCESO INICIA CON EL PLANTEAMIENTO DEL PROBLEMA DEL CUAL DEPENDERA LA RECOLECCION DE INFORMACION, DISEÑAR EL METODO APROPIADO DE RECOLECCION DE INFORMACION
#IDENTIFICAR LOS PROBLEMAS O FUENTES DE ERROR EN EL METODO DE RECOLECCION Y FINALMENTE LA RECOLECCION DE INFORMACION
#ES DE SUMA IMPORTANCIA TENER UN PLAN DE TRABAJO EN EL QUE SE DESCRIBAN DE MANERA PRECISA LAS TECNICAS A UTILIZAR PARA LLEVAR A CABO EL ANALISIS, YA QUE DE LOS CONTRARIO SE PUEDEN OBTENER CONCLUSIONES ERRONEAS.

#CONCEPTOS BASICOS DEL DISEÑO DE EXPERIMENTOS
#VARIABLE(S) INDEPENDIENTE(S):LA O LAS VARIABLES QUE SE ESPERA INCIDAN EN EL COMPORTAMIENTO DE LA VARIABLE DE INTERES.
#VARIABLE DEPENDIENTE:LA VARIABLE DE INTERES, QUE SE ESPERA SEA AFECTADA POR LAS VARIACIONES EN LA(S) VARIABLE(S) INDEPENDIENTE(S)
#HIPOTESIS:ES UNA SUPOSICION QUE BUSCA EXPLICAR EL COMPORTAMIENTO DE UN FENOMENO.
#TAMAÑO DE LA MUESTRA:ES EL NUMERO DE OBSERVACIONES EXPERIMENTALES QUE SE INCLUIRAN EN EL ANALISIS
#DISTORSIONADOR:VARIABLE EXOGENA AL ANALISIS QUE INCIDE EN LA RELACION ENTRE LA(S) VARIABLE(S) INDEPENDIENTE(S) Y DEPENDIENTE, CUYA EXCLUSION PRODUCE CONCLUSIONES ERRONEAS
#VARIABLE DE CONTROL:VARIABLE SOBRE LA CUAL EL INVESTIGADOR TIENE INCIDENCIA Y LE PERMITE REDUCIR EN CIERTO GRADO LOS EFECTOS ADVERSOS ASOCIADOS AL DISTORSIONADOR



