#OTRO DE LOS BENEFICIOS DE R MARKDOWN S QUE INTEGRA LAS FUNCIONALIDADES DE TRABAJAR EN CONTROL DE CAMBIOS, PERMITIENDO IDENTIFICAR PUNTUALMENTE LAS MODIFICACIONES REALIZADAS A UN DOCUMENTO.

