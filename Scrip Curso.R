                # CONTROL DE CAMBIOS EN R

# PERMITE RECONOCER QUE MODIFICACIONES SE HAN RELIZADO A UN CODIGO, CUANDO Y QUIEN LAS REALIZO
# AL REALIZAR UNA MODIFICACIÓN EL CONTROL DE CAMBIOS GUARDA UNA CAPTURA DE LA VERSIÓN ORIGINAL DEL CODIGO
# PEMITIENDO REGRESAR A LA VERSION ORIGINAL EN CASO DE SER NECESARIO

# EXISTEN DISTINTOS SISTEMAS QUE PERMITEN REGISTRAR EL CONTROL DE CAMBIOS EN UN CODIGO.
# UNO DE ESTOS SISTEMAS ES GIT.

# VENTAJAS DE UTILIZAR EL CONTROL DE CAMBIOS

# 1.PERMITE INCLUIR EN UN SOLO ARCHIVO TODAS LAS REVISIONES, ELIMINANDO LA NECESIDAD DE CONTAR CON MULTIPLES VERSIONES DE UN DOCUMENTO.
# 2.ELIMINA LOS ERRORES ASOCIADOS A LA UTILIZACION DE UNA VERSION EQUIVOCADA DEL DOCUMENTO.
# 3.PERMITE QUE MULTIPLES PERSONAS TRABAJEN EN UN MISMO DOCUMENT, UNIENDO LAS VERSIONES DEL DOCUMENTO ELABORADAS POR CADA PERSONA EN UN SOLO DOCUMENTO.
# 4.PERMITE GUARDAR COPIAS CON LA VERSION MAS ACTUALIZADA DE MANERA LOCAL, PARAR SU MODIFICACION Y POSTERIOR ENVIO A UN REPOSITORIO COMUN.
# 5.GUARDA UN REGISTRO DE TODOS LOS CAMBIOS REALIZADOS A UN DOCUMENTO, PERMITIENDO REGRESAR A VERSIONES ANTERIORES EN CASO DE ERRORES O CORRECCIONES.

#GIT VS GITHUB

#GIT ES UN SOFTWARE LOCAL ENCARGADO DE REGISTRAR EL CONTROL DE CAMBIOS.
#GITHUB ES UN REPOSITORIO EN LINEA EN EL CUAL SE GUARDAN LOS ARCHIVOS Y LOS CAMBIOS REALIZADOS EN ESTOS ARCHIVOS.

# VOCABULARIO DE GITHUB

#REPOSITORY: ES LA CARPETA EN LA CUAL SE ALMACENAN LOS ARCHIVOS DE TRABAJO Y EL HISTORIAL DE CAMBIOS.
#COMMIT: ES EL REGISTRO DE CADA CAMBIO REALIZADO AL ARCHIVO. AL REALIZAR CAMBIOS A UN ARCHIVO SE DEBE AGREGAR UNA NOTA QUE DESCRIBA LOS CAMBIOS REALIZADOS Y SU FINALIDAD.
#PUSH: ENVIA AL REPOSOTORIO LOS CAMBIOS REALIZADOS EN EL ARCHIVO.
#PULL: DESCARGA DEL REPOSITORIO EL ARCHIVO MAS RECIENTE
#STAGING: EN CASO DE CONTAR CON MAS DE UNA VERSION MODIFICADA DEL ARCHIVO, PARA LA CUALES SE QUIERE INDICAR EXPLICITAMENTE LOS CAMBIOS DE UNA A OTRA VERSION SE DEBEN SUBIR LOS ARCHIVOS EN FORMA ESCALONADA ACOMPAÑADOS DE LAS DESCRIPCIÓN CORRESPONDIENTE.
#BRANCH: SE DENOMINA DE ESTA FORMA A LA SITUACION EN LA CUAL SE CUENTA CON DOS COPIAS DE UN ARCHIVO PARA TRABAJAR DE FORMA SIMULTANEA FUERA DEL REPOSITORIO.
#MERGE: ES LA ACCION DE COMBINAR DOS O MAS VERSIONES DE UN ARCHIVO
#CONFLICT: SE PRESENTA CUANDO DOS O MAS USUARIOS REALIZAN MODIFICACIONES A UN ARCHIVO AL MISMO TIEMPO. EN ESTA SITUACION GIT ES INCAPAZ DE IDENTIFICAR EL ORDEN PARA UNIRLOS POR LO CUAL SE ACTIVA LA OPCION DE UNION MANUAL.
#CLONE: ACCION POR LA CUAL SE REALIZA UNA COPIA DE UN REPOSITORIO.
#FORK: PERMITE GUARDAR UNA COPIA PERSONAL DE UN REPOSITORIO EN LA CUAL LOS CAMBIOS REALIZADOS A UN ARCHIVO NO SE REFLEJAN EN EL REPOSITORIO COMPARTIDO. 

