##        DE ESTA FORMA GIT AUTOCOMPLETA LA RUTA SIN ERRORES.
##    B2)TRUCO: PARA REGRESAR UN NIVEL EN LA RUTA INGRESAR EL COMANDO CD ..
##    B3)TRUCO: PARA REGRESAR DOS NIVELES EN LA RUTA INGRESAR EL COMANDO CD ../..
##    B4)TRUCO: PARA BORRAR LA DIRECCION A LA DERECHA DEL CURSOR OPRIMIR CTRL+K
##    B5)TRUCO: PARA BORRAR LA DIRECCION A LA IZQUIERDA DEL CURSOR OPRIMIR CTRL+U
