## D)LS:LISTA LOS ARCHIVOS Y CARPETAS EN ESA DIRECCION
## E)LS -L:LISTA LOS ARCHIVOS Y CARPETAS EN ESA DIRECCION (ORDENADO EN COLUMNAS)
## F)LS -L (LETRA)*:LISTA LOS ARCHIVOS Y CARPETAS EN ESA DIRECCION QUE INICIAN CON ESA LETRA 
## G)LS -A (LETRA)*:LISTA TODOS LOS ARCHIVOS Y CARPETAS EN ESA DIRECCION INCLUSO ARCHIVOS OCULTOS
## H)CTRL+L: LIMPIA LA PANTALLA, PERO CONSERVA EL COMANDO QUE SE ESTE ESCRIBIENDO EN ESE MOMENTO
## I)CLEAR: LIMPIA LA PANTALLA POR COMPLETO
## J)PARA INDICAR UNA RUTA DE MANERA RAPIDA: ABRIR EL EXPLORADOR DE ARCHIVOS->SELECCIONAR LA CARPETA DE TRABAJO DESEADA CON CLICK DERECHO Y ELEGIR LA OPCION "GIT BASH HERE"

