                                ## LECCION 5
                  ## CATEGORIAS DEL ANALISIS DE DATOS

#EL ANALISIS DE DATOS PUEDE DIVIDIRSE EN SEIS CATEGORIAS PRINCIPALES:
# 1.DESCRIPTIVO
  #EL OBJETIVO DE ESTE TIPO DE ANALISIS CONSISTE EN PRESENTAR UN RESUMEN DE UN CONJUNTO DE DATOS. PARA LOGRAR LO ANTERIOR UTILIZA MEDIDAS DE TENDENCIA CENTRAL O DE DISPERSION.
  #ES IMPORTANTE MENCIONAR QUE ESTE ANALISIS NO IMPLICA LA INFERENCIA DE VALORES POBLACIONALES O LA ELABORACION DE CONCLUSIONES CON LA INFORMACION EXTRAIDA DEL CONJUNTO DE DATOS.

# 2.EXPLORATORIO
  #EL OBJETIVO DE ESTE TIPO DE ANALISIS CONSISTE EN EXAMINAR O EXPLORAR EL CONJUNTO DE DATOS PARA ENCONTRAR RELACIONES QUE PREVIAMENTE NO HABIAN SIDO SEÑALADAS.
  #ESTE TIPO DE ANALISIS SE ENFOCA EN EXPLORAR COMO DISTINTAS MEDICIONES PUEDEN RELACIONARSE CON OTRAS, SIN EMBARGO, NO CONFIRMA QUE ESTAS RELACIONES IMPLIQUEN CAUSALIDAD.
  #PERMITE IDENTIFICAR RELACIONES ENTRE VARIABLES PERO NO RELACIONES CAUSALES
  #DEBIDO A QUE ESTE TIPO DE ANALISIS SE ENFOCA EN LA CORRELACION ENTRE GRUPOS DE VARIABLES SE DEBE SER CUIDADOSO Y NO TOMAR LOS RESULTADOS OBTENIDOS COMO VERDADES UNIVERSALES,YA QUE ES CONOCIDO QUE LA CORRELACION DE DOS O MAS VARIABLES NO IMPLICA CAUSALIDAD

# 3.INFERENCIAL
  #EL OBJETIVO DE ESTE TIPO DE ANALISIS CONSISTE EN REALIZAR INFERENCIAS SOBRE PARAMETROS POBLACIONALES A PARTIR DE DATOS MUESTRALES
  #SE APOYA DE TECNICAS ESTADISTICAS PARA LA CREACION DE MODELOS QUE PERMITAN REALIZAR INFERENCIAS
  #PERMITE CONOCER EL GRADO DE INCERTIDUMBRE AL CUAL SE ENFRENTAN LAS INFERENCIAS REALIZADAS
  #UTILIZA INFORMACION MUESTRAL COMO INSUMO BASICO
  #LA PRECISION DE LAS INFERENCIAS REALIZADAS DEPENDE DE LA CALIDAD DE LOS DATOS UTILIZADOS COMO INSUMOS

# 4.PREDICTIVO
  #EL OBJETIVO DE ESTE TIPO DE ANALISIS CONSISTE EN PREDECIR EL COMPORTAMIENTO FUTURO DE UNA O MAS VARIABLES UTILIZANDO LA INFORMACION DE SUS VALORES PASADOS Y ACTUALES
  #PERMITE CONOCER EL GRADO DE INCERTIDUMBRE AL CUAL SE ENFRENTAN LAS PREDICCIONES REALIZADAS
  #UTILIZA INFORMACION MUESTRAL COMO INSUMO BASICO
  #LA PRECISION DE LAS PREDICCIONES REALIZADAS DEPENDE DE LA CALIDAD DE LOS DATOS UTILIZADOS COMO INSUMOS
  #ESTE TIPO DE ANALISIS TAMPOCO SE ENFOCA EN DESCRIBIR LAS RELACIONES CAUSALES ENTRE LOS DATOS
  
# 5.CAUSAL
  #EL OBJETIVO DE ESTE TIPO DE ANALISIS CONSISTE EN ANALIZAR E IDENTIFICAR LA EXISTENCIA DE RELACIONES CAUSALES ENTRE UN CONJUNTO DE DATOS
  #SE ENFOCA EN ANALIZAR EL COMPORTAMIENTO DE UNA VARIABLE AL MANIPULAR UNA O MAS VARIABLES
  #ES UN TIPO DE ANALISIS COMPLICADO CUYOS RESULTADOS DEPENDEN AMPLIAMENTE DE LOS DATOS UTILIZADOS, LAS RELACIONES ANALIZADAS Y LOS SUPUESTOS EN LOS QUE SE FUNDAMENTA EL ANALISIS.
  #TRABAJA CON DATOS AGREGADOS POR LO CUAL LAS CONCLUSIONES EXTRAIDAS PUEDEN NO SE APLICABLES A CIERTOS SUBMUESTRAS DE LA POBLACION

# 6.MECANISTICO
  #SE ENFOCA EN ANALIZAR LOS CAMBIOS EXACTOS EXPERIMENTADOS POR UNA VARIABLE AL REALIZAR UN CAMBIO EXACTO EN OTRA VARIABLE.
  #TRABAJA CON DATOS EXPERIMENTALES
  #ES APLICADO PRINCIPALMENTE EN CIENCIAS EXACTAS
  #EMPLEA TECNICAS DE MODELACION DETERMINISTA
  #AL CONTROLAR LAS VARIABLES QUE GENERAN LOS DATOS SE INCREMENTA LA CALIDAD DE LOS DATOS OBTENIDOS
  #EL "UNICO" INCONVENIENTE AL QUE SE ENFRENTA ES LA LOS ERRORES DE MEDICION EN LOS DATOS

